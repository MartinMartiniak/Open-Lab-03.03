using System;

namespace Open_Lab_03._03
{
    public class Comparator
    {
        public bool CompareCharactersCount(string str1, string str2)

        {
            int a;
            {
                a = str1.Length;
                {
                    int b;
                    {
                        b = str2.Length;
                        {
                            return a - b == 0;
                        }
                    }
                }
            }
        }
    }
}
